    Object? name = _unset,
    Object? uomType = _unset,
    Object? uom = _unset,
    Object? coverageRate = _unset,
    Object? dims = _unset,
  }) async {
    final patch = <String, Object?>{};
    if (!identical(name, _unset)) {
      final cleaned = (name as String?)?.trim();
      if (cleaned == null || cleaned.isEmpty) return;
      patch['name'] = cleaned;
    }
    if (!identical(uomType, _unset)) {
      patch['uom_typ'] = (uomType as UomType?)?.db;
    }
    if (!identical(uom, _unset)) {
      final cleaned = (uom as String?)?.trim();
      patch['uom'] = (cleaned == null || cleaned.isEmpty) ? null : cleaned;
    }
    if (!identical(coverageRate, _unset)) {
      patch['cov_rate'] = coverageRate as double?;
    }
    if (!identical(dims, _unset)) {
      final d = dims as MaterialDims?;
      patch['dims'] = (d == null || d.isEmpty) ? null : jsonEncode(d.toJson());
    }
    if (patch.isEmpty) return;
    await _db
        .update('material_types', patch, where: 'id = ?', whereArgs: [id]);
  }

  /// Backwards-compatible helper for callers that only need to rename.
  Future<void> renameMaterialType(String id, String newName) =>
      updateMaterialType(id, name: newName);

  /// Deletes a material type. Every row is freely deletable now —
  /// historical material_inventory rows store the type name as a free-form
  /// string, so removing the catalog row only hides the option from the
  /// Buy Material dropdown going forward.
  Future<bool> deleteMaterialType(String id) async {
    final n = await _db.delete(
      'material_types',
      where: 'id = ?',
      whereArgs: [id],
    );
    return n > 0;
  }

  Future<List<MaterialItem>> materialInventory({String? projectId}) async {
    // Excludes rows whose parent journal txn has been soft- or hard-
    // deleted (v13 linkage). Add an `includeDeleted` flag here if a
    // future audit screen needs to show them.
    final whereClauses = <String>['is_deleted = 0'];
    final args = <Object>[];
    if (projectId != null) {
      whereClauses.add('project_id = ?');
      args.add(projectId);
    }
    final rows = await _db.query(
      'material_inventory',
      where: whereClauses.join(' AND '),
      whereArgs: args,
      orderBy: 'created_at DESC',
    );
    return rows.map(MaterialItem.fromMap).toList();
  }

  // ---- Labour Types ----

  Future<List<LabourTypeDef>> labourTypes() async {
    final rows = await _db.query('labour_types', orderBy: 'name ASC');
    return rows.map(LabourTypeDef.fromMap).toList();
  }

  Future<LabourTypeDef?> addLabourType(
    String name, {
    String? description,
    double? defaultDailyRate,
  }) async {
    final clean = name.trim();
    if (clean.isEmpty) return null;
    final existing = await _db.query(
      'labour_types',
      where: 'LOWER(name) = LOWER(?)',
      whereArgs: [clean],
      limit: 1,
    );
    if (existing.isNotEmpty) {
      throw StateError('A labour type named "$clean" already exists.');
    }
    final row = LabourTypeDef(
      id: _uuid.v4(),
      name: clean,
      description: description?.trim().isEmpty == true ? null : description?.trim(),
      defaultDailyRate: defaultDailyRate,
      createdAt: DateTime.now().toUtc(),
    );
    await _db.insert('labour_types', row.toMap());
    return row;
  }

  Future<void> updateLabourType(
    String id, {
    Object? name = _unset,
    Object? description = _unset,
    Object? defaultDailyRate = _unset,
  }) async {
    final patch = <String, Object?>{};
    if (!identical(name, _unset)) {
      final cleaned = (name as String?)?.trim();
      if (cleaned == null || cleaned.isEmpty) return;
      patch['name'] = cleaned;
    }
    if (!identical(description, _unset)) {
      final cleaned = (description as String?)?.trim();
      patch['description'] = (cleaned == null || cleaned.isEmpty) ? null : cleaned;
    }
    if (!identical(defaultDailyRate, _unset)) {
      patch['default_daily_rate'] = defaultDailyRate as double?;
    }
    if (patch.isEmpty) return;
    await _db.update('labour_types', patch, where: 'id = ?', whereArgs: [id]);
  }

  Future<bool> deleteLabourType(String id) async {
    final n = await _db.delete('labour_types', where: 'id = ?', whereArgs: [id]);
    return n > 0;
  }

