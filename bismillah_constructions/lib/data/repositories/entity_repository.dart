  /// Delete a bank IFF it has never been used in a journal entry. Otherwise
  /// throws — historical balances must keep referring to a real account.
  /// Most callers should use [archiveBank] instead so the audit trail stays
  /// intact.
  Future<void> deleteBank(String id) async {
    final used = await _db.rawQuery(
      'SELECT 1 FROM journal_entries WHERE account_id = ? LIMIT 1',
      [id],
    );
    if (used.isNotEmpty) {
      throw StateError(
          'Bank/wallet $id is referenced by journal entries and cannot be deleted. Archive it instead.');
    }
    await _db.delete('banks', where: 'id = ?', whereArgs: [id]);
  }

