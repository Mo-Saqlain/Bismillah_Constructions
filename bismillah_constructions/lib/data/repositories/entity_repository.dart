  /// Soft-archive: preserves data, removes from active lists. Logged.
  ///
  /// **Gate**: every supplier owed money on this project must be settled
  /// (`supplierPayables == 0`). The previous design also forced
  /// `ledgerNet == 0` (total costs = total revenue), but that requirement
  /// is wrong for fixed-price With-Material contracts — the contractor's
  /// **profit** is the gap between revenue and costs, so insisting it be
  /// zero would mean refunding the profit before close. The PoC revenue
  /// recognition + loss-provision logic in
  /// [LedgerRepository.incomeFigures] already surfaces over-budget jobs
  /// and unearned customer deposits, so the user has visibility without
  /// the archive being blocked.
  ///
  /// Throws [ReconciliationException] when supplier payables are open.
  ///
  /// Pass `force: true` to bypass — only used for migrations or
  /// data-repair flows.
  Future<void> archiveProject(String id,
      {String? note, bool force = false}) async {
    final p = await project(id);
    if (p == null) return;

    if (!force) {
      final balance = await _projectLedgerBalance(id);
      if (balance.supplierPayables.abs() >= 0.01) {
        throw ReconciliationException(
          projectId: id,
          outstandingPayables: balance.supplierPayables,
          netLedger: balance.ledgerNet,
        );
      }

      // With-Material gate: if the customer has paid less than budget, the
      // user has to either record the remaining payment or shrink the
      // budget to match what was actually received. Overpayments
      // (received > budget) are allowed through — the excess is already
      // surfaced as a "deposit owed back" liability.
      if (p.model == ProjectModel.withMaterial &&
          (p.budget ?? 0) > 0 &&
          balance.received + 0.01 < (p.budget ?? 0)) {
        throw ProjectBudgetMismatchException(
          projectId: id,
          budget: p.budget!,
          received: balance.received,
        );
      }

      // Labour-Rate gate: customer money is a deposit, NOT contractor
      // income. After the service-fee reclassification, the project
      // ledger must net to zero — surplus has to be refunded to the
      // customer, deficit collected from them. Otherwise archiving
      // leaves unresolved customer money in the books.
      //
      // This is a deliberate re-introduction of the old "ledgerNet == 0"
      // requirement, but scoped only to LR. WM doesn't need it (revenue
      // legitimately exceeds costs in a fixed-price contract — that's
      // the profit).
      if (p.model == ProjectModel.labourRate &&
          balance.ledgerNet.abs() >= 0.01) {
        throw LabourRateUnsettledException(
          projectId: id,
          // `netLedger = costs − revenue`. A negative value means revenue
          // exceeds costs (surplus to refund); positive means costs
          // exceed revenue (customer underpaid, owes us). Express as
          // `netToSettle = −netLedger` so positive = refund the customer
          // (matches the LabourRateClose sign convention).
          netToSettle: -balance.ledgerNet,
        );
      }
    }

    final now = DateTime.now().toUtc();
    final dev = await _deviceId();
    await _db.transaction((txn) async {
      await txn.update(
        'projects',
        {'is_archived': 1, 'archived_at': now.toIso8601String()},
        where: 'id = ?',
        whereArgs: [id],
      );
      await txn.insert(
          'change_log',
          ChangeLog(
            id: _uuid.v4(),
            entityType: 'project',
            entityId: id,
            action: ChangeAction.archive,
            originalData: jsonEncode(p.toMap()),
            note: note,
            deviceId: dev,
            timestamp: now,
          ).toMap());
    });
  }

  /// Computes both gates that have to be satisfied before a project can be
  /// archived:
  ///
  ///   * `supplierPayables` — outstanding credit balance of
  ///     `Supplier Payables` for this project. Must be 0 (every credited
  ///     bill paid down).
  ///   * `ledgerNet` — net of the project ledger as the user sees it on
  ///     the Project Ledger screen: cost-side debits (Material, Labour)
  ///     minus credit-side income (Project Revenue, Service Fee Income).
  ///     Must be 0 — every rupee charged to the project has to be
  ///     cancelled by an inflow before archive.
  Future<_ProjectLedgerBalance> _projectLedgerBalance(String projectId) async {
    Future<double> sum(String column, String accountId) async {
      final rows = await _db.rawQuery(
        'SELECT COALESCE(SUM($column),0) AS s FROM journal_entries '
        'WHERE account_id = ? AND project_id = ? AND is_deleted = 0',
        [accountId, projectId],
      );
      return ((rows.first['s'] as num?) ?? 0).toDouble();
    }

    final supplierPaid = await sum('debit', Accounts.supplierPayables.id);
    final supplierCredits = await sum('credit', Accounts.supplierPayables.id);
    final supplierPayables = supplierCredits - supplierPaid;

    final materialDr = await sum('debit', Accounts.materialCosts.id);
    final labourDr = await sum('debit', Accounts.labourCosts.id);
    final revenueCr = await sum('credit', Accounts.projectRevenue.id);
    final feeCr = await sum('credit', Accounts.serviceFeeIncome.id);
    // For a balanced Labour-Rate project, customer money in must equal
    // customer money out:
    //     PROJECT_REV credits == costs incurred + fee charged
    //     revenueCr == (materialDr + labourDr) + feeCr
    // Rearranged into "net" form:
    //     (materialDr + labourDr + feeCr) − revenueCr == 0
    //
    // The previous formula was `(matDr + labDr) − (revCr + feeCr)` —
    // that double-counted the fee, because `postProjectServiceFee` is a
    // reclassification (Dr PROJECT_REV / Cr SERVICE_FEE_INCOME): the fee
    // is already taken out of revenue, so adding it back to the income
    // side made balanced LR projects look 2×feeCr off.
    final ledgerNet =
        (materialDr + labourDr + feeCr) - revenueCr;

    return _ProjectLedgerBalance(
      supplierPayables: supplierPayables,
      ledgerNet: ledgerNet,
      received: revenueCr,
    );
  }

