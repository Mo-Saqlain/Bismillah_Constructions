/// Internal struct for [EntityRepository._projectLedgerBalance]. Exists
/// purely to bundle the two gating numbers the archive guard needs.
class _ProjectLedgerBalance {
  final double supplierPayables;
  final double ledgerNet;
  /// Total project revenue credits (sum of all "Received from project"
  /// transactions, net of any service-fee reclassification).
  final double received;
  const _ProjectLedgerBalance({
    required this.supplierPayables,
    required this.ledgerNet,
    required this.received,
  });

  /// Gate fails when either side has not been zeroed out (1 paisa
  /// tolerance to absorb floating-point noise from divisions).
  bool get isOutOfBalance =>
      supplierPayables.abs() >= 0.01 || ledgerNet.abs() >= 0.01;
}

/// Thrown when an attempt is made to archive a project whose ledger has
/// not been fully reconciled. Carries both the outstanding supplier
/// payable balance and the project ledger net so the UI can tell the
/// user what's wrong.
class ReconciliationException implements Exception {
  final String projectId;
  final double outstandingPayables;
  final double netLedger;
  const ReconciliationException({
    required this.projectId,
    required this.outstandingPayables,
    this.netLedger = 0,
  });
  @override
  String toString() => 'ReconciliationException(project=$projectId, '
      'outstandingPayables=$outstandingPayables, netLedger=$netLedger)';
}

/// Thrown when a Labour-Rate project is being archived but the project
/// ledger has not been balanced to zero — i.e. customer money is still
/// unsettled. For an LR project the contractor only earns the service
/// fee; everything else is pass-through, so any surplus must be refunded
/// and any deficit collected before close.
///
/// `netToSettle` follows the same sign convention as [LabourRateClose]:
///   * Positive → contractor is holding the customer's surplus → refund.
///   * Negative → customer underpaid → collect the deficit.
class LabourRateUnsettledException implements Exception {
  final String projectId;
  final double netToSettle;
  const LabourRateUnsettledException({
    required this.projectId,
    required this.netToSettle,
  });
  double get refundToCustomer => netToSettle > 0 ? netToSettle : 0;
  double get customerOwesUs => netToSettle < 0 ? -netToSettle : 0;
  @override
  String toString() => 'LabourRateUnsettledException(project=$projectId, '
      'netToSettle=$netToSettle)';
}

/// Thrown when a With-Material project is being archived but the customer
/// has paid less than the budget — i.e. there is still money outstanding
/// or the contract value was effectively renegotiated downward. The UI
/// catches this and offers to adjust the budget to match the received
/// amount (resizing the contract) or cancel.
///
/// `received > budget` is **not** a mismatch for this purpose: the excess
/// is already tracked as a refund-able customer deposit by the PoC logic
/// in [LedgerRepository.incomeFigures], and archiving with that liability
/// in place is the correct accounting outcome.
class ProjectBudgetMismatchException implements Exception {
  final String projectId;
  final double budget;
  final double received;
  const ProjectBudgetMismatchException({
    required this.projectId,
    required this.budget,
    required this.received,
  });
  double get shortfall => budget - received;
  @override
  String toString() => 'ProjectBudgetMismatchException(project=$projectId, '
      'budget=$budget, received=$received)';
}

/// Thrown when an attempt is made to archive a supplier whose payable
/// balance is not zero. The UI catches this and tells the user how much
/// is still owed (or owed-to-us).
class SupplierNotSettledException implements Exception {
  final String supplierId;
  final double outstandingBalance;
  const SupplierNotSettledException({
    required this.supplierId,
    required this.outstandingBalance,
  });
  @override
  String toString() => 'SupplierNotSettledException(supplier=$supplierId, '
      'balance=$outstandingBalance)';
}
