  Future<void> updateSupplierFields(
    String id, {
    String? name,
    String? phone,
    SupplierCategory? category,
    String? taxStatus,
    String? bankDetails,
  }) async {
    final updates = <String, Object?>{};
    if (name != null) updates['name'] = name.trim();
    if (phone != null) {
      updates['phone'] = phone.trim().isEmpty ? null : phone.trim();
    }
    if (category != null) updates['category'] = category.db;
    if (taxStatus != null) {
      updates['tax_status'] =
          taxStatus.trim().isEmpty ? null : taxStatus.trim();
    }
    if (bankDetails != null) {
      updates['bank_details'] =
          bankDetails.trim().isEmpty ? null : bankDetails.trim();
    }
    if (updates.isEmpty) return;

    final before = await supplier(id);
    if (before == null) return;
    final dev = await _deviceId();
    final now = DateTime.now().toUtc();
    await _db.transaction((txn) async {
      await txn.update('suppliers', updates,
          where: 'id = ?', whereArgs: [id]);
      await txn.insert(
          'change_log',
          ChangeLog(
            id: _uuid.v4(),
            entityType: 'supplier',
            entityId: id,
            action: ChangeAction.edit,
            originalData: jsonEncode(before.toSupplierMap()),
            newData: jsonEncode(updates),
            deviceId: dev,
            timestamp: now,
          ).toMap());
    });
  }

