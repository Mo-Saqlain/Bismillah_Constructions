  /// Cached device_id, fetched lazily from `app_settings` and reused for every
  /// audit write. The value is generated by `BackupService.ensureDeviceId` on first run.
  String? _cachedDeviceId;
  Future<String?> _deviceId() async {
    if (_cachedDeviceId != null) return _cachedDeviceId;
    final v = await getSetting('device_id');
    _cachedDeviceId = v;
    return v;
  }

